[Unlayer](https://unlayer.com) is a beautiful editor that allows you to edit html in a structured way. You don't need any HTML knowledge to compose a campaign or template. This editor also allows image uploads.

Mailcoach uses the free version of Unlayer.

<div class="alert alert-warning max-w-xl">
    Unlayer editor stores content in a structured way. When switching from or to the Unlayer editor content in existing templates and draft campaigns will get lost.
</div>
